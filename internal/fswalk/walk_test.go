func TestWalkLanguageDetection(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, dir, "main.go", "package main")
	writeFile(t, dir, "app.ts", "export const x = 1")
	writeFile(t, dir, "app.tsx", "export const x = 1")
	writeFile(t, dir, "util.js", "module.exports = {}")
	writeFile(t, dir, "util.jsx", "module.exports = {}")

	candidates, err := Walk(dir, []string{"**/*.go", "**/*.ts", "**/*.tsx", "**/*.js", "**/*.jsx"}, nil, 0)
	if err != nil {
		t.Fatal(err)
	}
	if len(candidates) != 5 {
		t.Errorf("expected 5 candidates, got %d", len(candidates))
	}

	langs := map[string]int{}
	for _, c := range candidates {
		langs[c.Language]++
	}
	if langs["go"] != 1 {
		t.Errorf("expected 1 go file, got %d", langs["go"])
	}
	if langs["typescript"] != 2 {
		t.Errorf("expected 2 typescript files, got %d", langs["typescript"])
	}
	if langs["javascript"] != 2 {
		t.Errorf("expected 2 javascript files, got %d", langs["javascript"])
	}
}

func TestWalkGeneratedFile(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, dir, "normal.go", "package main")
	writeFile(t, dir, "gen.go", "// Code generated by tool; DO NOT EDIT.\npackage main")
	if err := os.MkdirAll(filepath.Join(dir, "generated"), 0o755); err != nil {
		t.Fatal(err)
	}
	writeFile(t, dir, "generated/stuff.go", "package gen")

	candidates, err := Walk(dir, []string{"**/*.go"}, nil, 0)
	if err != nil {
		t.Fatal(err)
	}

	genCount := 0
	for _, c := range candidates {
		if c.IsGenerated {
			genCount++
		}
	}
	if genCount != 2 {
		t.Errorf("expected 2 generated files, got %d", genCount)
	}
}

func TestStatCandidateHit(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, dir, "main.go", "package main")

	c, ok := StatCandidate(dir, "main.go", []string{"**/*.go"}, nil, 0)
	if !ok {
		t.Fatal("expected StatCandidate to succeed")
	}
	if c.Path != "main.go" || c.Language != "go" {
		t.Errorf("bad candidate: %+v", c)
	}
	if c.AbsPath != filepath.Join(dir, "main.go") {
		t.Errorf("AbsPath = %q, want %q", c.AbsPath, filepath.Join(dir, "main.go"))
	}
	if c.Size == 0 || c.ModTime == 0 {
		t.Errorf("stat fields not populated: size=%d mtime=%d", c.Size, c.ModTime)
	}
}

